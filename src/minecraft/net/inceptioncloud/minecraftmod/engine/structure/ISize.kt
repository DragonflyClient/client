package net.inceptioncloud.minecraftmod.engine.structure

/**
 * ## Size Interface
 *
 * The size interface is used for widgets that have the same width and height. This value is
 * specified by the [size] variable.
 */
interface ISize
{
    /**
     * The size of the object.
     *
     * It specifies both the width and the height of the widget.
     */
    var size: Double
}