package net.inceptioncloud.minecraftmod.version;

/**
 * Provides all information about the current InceptionCloud version.
 */
public class InceptionCloudVersion
{
    /**
     * The first part of the title.
     */
    public static final String TITLE_1 = "Inception";

    /**
     * The second part of the title.
     */
    public static final String TITLE_2 = "Cloud";

    /**
     * The version in number format.
     */
    public static final String VERSION = "2.0";

    /**
     * The name of the current version (called identifier).
     */
    public static final String IDENTIFIER = "Friday";

    /**
     * The full version string.
     */
    public static final String FULL_VERSION = TITLE_1 + TITLE_2 + " " + VERSION + " " + IDENTIFIER;
}
